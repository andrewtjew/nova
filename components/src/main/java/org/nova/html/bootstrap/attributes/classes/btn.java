package org.nova.html.bootstrap.attributes.classes;

public class btn extends Class<btn>
{
	public btn()
	{
		super("btn");
	}

	public btn default_()
	{
		return add("btn-default");
	}

	public btn primary()
	{
		return add("btn-primary");
	}

	public btn success()
	{
		return add("btn-success");
	}

	public btn info()
	{
		return add("btn-info");
	}

	public btn warning()
	{
		return add("btn-warning");
	}

	public btn danger()
	{
		return add("btn-danger");
	}

	public btn link()
	{
		return add("btn-link");
	}

}
