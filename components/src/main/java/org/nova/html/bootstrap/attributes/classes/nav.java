package org.nova.html.bootstrap.attributes.classes;

public class nav extends Class<nav>
{
	public nav()
	{
		super("nav");
	}

	public nav tabs()
	{
		return add("nav-tabs");
	}
	public nav pills()
	{
		return add("nav-pills");
	}
	public nav justified()
	{
		return add("nav-justified");
	}
	public nav stacked()
	{
		return add("nav-stacked");
	}
	
}
